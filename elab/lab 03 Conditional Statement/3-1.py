# Boolean Expression: Relational Operators

import math

# Relational Operator
# > < >= <= != ==  // ตรรกศาสตร์
print(math.pi == 3.14) # Output : False

a = 90
b = 90.0
print(a > b) # Output : False

a = 90
b = 90.0
print(a <= b) # Output : True // not same datatype

print('abcdefg' > 'b') # Output : False // str can't change range of it

print('B' == 'b') # Output : False

print('9' < '123456') # Output : False


